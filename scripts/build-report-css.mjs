import { execFileSync } from "node:child_process";
import { mkdtempSync, readFileSync, writeFileSync, rmSync } from "node:fs";
import { tmpdir } from "node:os";
import { join } from "node:path";

const tmp = mkdtempSync(join(tmpdir(), "copilot-report-css-"));
const out = join(tmp, "report.css");
try {
  execFileSync(
    "npx",
    ["tailwindcss", "-i", "src/report.css", "-o", out, "--minify"],
    { stdio: "inherit", shell: process.platform === "win32" }
  );
  const css = readFileSync(out, "utf8");
  writeFileSync(
    "src/report-style.ts",
    `// Generated by scripts/build-report-css.mjs. Do not edit by hand.\nexport const REPORT_CSS = ${JSON.stringify(css)};\n`
  );
} finally {
  rmSync(tmp, { recursive: true, force: true });
}
